	Mata ajaran ini membahas kebutuhan dan kecukupan gizi termasuk kecukupan gizi berbagai kelompok fisiologis sesuai dengan daur kehidupan. Kemudian dibahas berbagai metode penentuan keadaan gizi pada tingkat masyarakat. Dibahas tentang beberapa issue mutakhir dalam bidang pangan dan gizi</p>
<p>
	<strong>IKM 51513</strong>&nbsp;<strong>Metodologi Penelitian 2 SKS&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </strong></p>
<p style="text-align: justify;">
	Mata ajaran ini membahas metode penelitian yang digunakan dalam memecahkan masalah Adminisantrasi dan Kebijakan Kesehatan, meliputi: penyusunan latar belakang, permasalahan, tujuan penelitian, landasan teori dan kerangka konsep, jenis penelitian, populasi dan sampel, metode pengumpulan data, definisi operasional, aspek pengukuran, teknik analisis data, hasil penelitian dan pembahasan, serta kesimpulan dan Saran. Setelah mengikuti mata kuliah ini mahasiswa dapat mengaplikasikan metode penelitian untuk penelitian ilmiah dan terapan bidang kesehatan masyarakat.</p>
<p>
	<strong>IKM 51613&nbsp; K3 dan Kesehatan Lingkungan 3 SKS</strong></p>
<p style="text-align: justify;">
	Mata ajaran ini membahas tentang konsep dasar&nbsp; kesehatan lingkungan dan kesehatan kerja, seperti pengertian kesehatan lingkungan dan kesehatan kerja, hubungan antar manusia dan lingkungan, isu-isu pencemaran lingkungan&nbsp; serta isu-isu pada kesehatan kerja dan keselamatan kerja.</p>
<p>
	<strong>IKM 69713 Seminar Proposal 2 SKS</strong></p>
<p style="text-align: justify;">
	Kegiatan terstruktur&nbsp; berupa seminar ilmiah dari proposal penelitian ataupun topik lain dengan persetujuan komisi dosen pembimbing.</p>
<p>
	<strong>IKM 69913 Thesis&nbsp;6 SKS</strong></p>
<p style="text-align: justify;">
	Mata ajaran ini membahas naskah tesis yang bersumber dari hasil penelitian ilmiah, yang disusun oleh mahasiswa dengan bimbingan tim pembimbing, dengan tujuan meningkatkan kualitas naskah tesis dan pemahaman mahasiswa tentang substansi naskah tesis. Komponen yang dibahas, meliputi : (1) Isi dan bobot tesis, mencakup keaslian ide atau gagasan, bobot permasalahan, landasan teori, metode penelitian, penulisan hasil penelitian, substansi pembahasan, perumusan kesimpulan dan saran, (2) Penyajian isi tesis secara lisan, (3) Kemampuan menjawab dan mempertahankan isi tesis, dan (4) Sikap dan penampil</p>
<p>
	<strong>BKU EPIDEMIOLOGI DAN BIOSTATISTIK</strong></p>
<p>
	<strong>KME 51113&nbsp;Metode dan Analisis Epidemiologi&nbsp;2 SKS&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; &nbsp;&nbsp; </strong></p>
<p style="text-align: justify;">
	Mata kuliah ini difokuskan pada penelitian dan kesehatan masyarakat dimana setiap mahasiswa diwajibkan untuk mengkaji hasil telaah dari suatu artikel. Secara umum mata ajar ini bertujuan untuk memberikan pemahaman kepada mahasiswa tentang sintesis berbagai metode epidemiologi yang digunakan pada pengembangan implementasi bidang kesehatan dan kesehatan masyarakat.</p>
<p>
	<strong>KME 51213 Manajemen dan Analisis Data Lanjut 2 SKS </strong></p>
<p style="text-align: justify;">
	Mata ajaran ini membahastentang manajemen data untuk menghasilkan informasi, yang meliputi: perancangan template untuk memasukan data dengan komputer, penggabungan data, modifikasi variabel, <em>transformasi </em>data <em>(recode/compute)</em>, impor dan ekspor data antar perangkat lunak pengolah data dan antara perangkat lunak statistik. Selanjutnya dipelajari tentang analisis univariat, bivariat dan multivariat untuk data dengan desain sampel acak sederhana dan non acak sederhana.</p>
<p>
	<strong>KME 51313&nbsp;Investigasi Wabah&nbsp;2 SKS&nbsp;&nbsp; </strong></p>
<p style="text-align: justify;">
	Mata kuliah ini mempelajari penyakit menular yang sering kali menimbulkan wabah terutama di Indonesia. Wabah ini meliputi kejadian luar biasa maupun letusan yang biasa saja tidak diakibatkan oleh penyakit menular namun juga penyakit tidak menular. Diharapkan setelah mengikuti mata ajaran ini mahasiswa diharapkan untuk menerapkan langkah-langkah investigasi wabah, mulai dari persiapan ke lapangan sampai pada penulisan laporan.</p>
<p>
	<strong>BKU ADMINISTRASI KEBIJAKAN KESEHATAN</strong></p>
<p>
	<strong>KMA 51113 Perencanaan,Implementasi dan Evaluasi Program Kesehatan Masyarakat 2 SKS</strong></p>
<p style="text-align: justify;">
	Mata kuliah ini membahas mengenai masalah gizi yang sedang maupun akan menjadi problem di Indonesia. Selain itu akan dibahas mengenai berbagai program kesehatan yang telah dilaksanakan maupun program-program yang masih dalam rangka uji coba di Indonesia, &nbsp;juga akan dibahas berbagai faktor yang menentukan keberhasilan program-program tersebut mulai dari perencanaan sampai dengan evaluasinya. Dibahas juga mengenai kebijakan pangan secara umum, pentingnya kebijakan pangan dan kaitannya dengan status gizi masyarakat.</p>
<p>
	<strong>KMA 51213&nbsp;Kepemimpinan Strategis&nbsp; dan Berfikir Sistem&nbsp;2 SKS </strong></p>
<p style="text-align: justify;">
	Mata kuliah ini memberikan wawasan, keterampilan teknis tentang kepemimpinan strategis, mulai dari menggunakan cara berpikir sistem, kemampuan untuk melakukan perubahan melalui peningkatan <em>personal mastery</em> dan selalu meninjau <em>mental model</em> yang digunakannya, mengkomunikasikan melalui dialog dan diskusi, dan menggerakkan orang melalui visi bersama dan pembelajaran tim, dan keterampilan teknis dalam mencari akar masalah. Integrasi seluruh konsep diberikan dalam bentuk latihan dan penugasan perorangan maupun kelompok.</p>
<p>
	<strong>KMA 51313 Mutu Pelayanan Kesehatan&nbsp;2 SKS&nbsp;&nbsp; </strong></p>
<p style="text-align: justify;">
	Di dalam mata kuliah ini dibahas mengenai perkembangan mutu secara umum, perkembangan mutu layanan kesehatan, konsep-konsep dasar mutu, Total <em>Quality Management</em> (TQM), transformasi budaya untuk perbaikan mutu, berbagai cara untuk mengukur berbagai dimensi.</p>
<p>
	<strong>BKU KESEHATAN KESELAMATAN KERJA (K3) DAN </strong></p>
<p>
	<strong>KESEHATAN LINGKUNGAN</strong></p>
<p>
	<strong>KMK 51113&nbsp;Analisis Risiko Kesehatan Lingkungan&nbsp;2 SKS </strong></p>
<p style="text-align: justify;">
	Mata ajar ini membahas prinsip, metode dan dalam menganalisis berapa besar kemungkinan risiko dan pencegahannya yang akan terjadi pada suatu kegiatan manusia, baik dengan analisis kualitatif maupun kuantitatif.&nbsp;</p>
<p>
	<strong>KMK 51213&nbsp; Toksikologi Lingkungan dan Industri&nbsp;2 SKS</strong></p>
<p style="text-align: justify;">
	Mata ajaran ini membahas aspek toksikologi dari bahan kimiawi yang lazim ditemui baik di lingkungan ambien maupun industri. Dimulai dengan tinjauan umum toksikologi, kemudian membahas secara spesifik bahan-bahan kimiawi terutama logam berat, pelarut (solvents), pestisida, bahan inorganic dan gas beracun. Dibicarakan juga cara-cara penilaian resiko dan pemantauan bahan-bahan kimiawi tersebut. Secara khusus akan dibahas teknik mutakhir dalam bidang penilaian risiko kesehatan yang disebut Bio Marker dan Biological Monitoring.</p>
<p>
	<strong>KMK 51313 Higiene Industri (Industrial Hygiene) 2 SKS </strong></p>
<p style="text-align: justify;">
	Mata kuliah ini membahas aspek bio-kimiawi-fisik dalam lingkungan industri dan pengaruhnya terhadap kesehatan pekerja serta cara-cara pengendaliannya. Konsep AREC akan dibahas secara mendalam serta teknologi yang terkait sesuai dengan best practice yang ada di Industri, khsusnya yang diadopsi oleh NIOSH, OSHA, dan HSE. Secara khusus akan dibahas cara-cara evaluasi faktor fisik, kimiawi, biologik dan ergnomik dalam lingkungan kerja industri. Kunjungan lapangan dan penugasan akan dilakukan ke Balai Hiperkes Sumsel, dan PT. Pusri.</p>
<p>
	<strong>MATA KULIAH PILIHAN </strong></p>
<p>
	<strong>SEMESTER 2 (4 SKS)</strong></p>
<p>
	<strong>IKM 51713</strong>&nbsp;<strong>Surveilans Epidemiologi 2 SKS</strong></p>
<p style="text-align: justify;">
	Mata ajaran ini membahas cara pengamatan masalah kesehatan yang berhubungan dengan mortalitas dan morbiditas dalam upaya menjelaskan pola dan karakteristiknya; cara mengevaluasi system surveilans dan meramalkan kejadian yang akan datang sesuai hasil pengamatan yang dilakukan.</p>
<p>
	<strong>IKM 51813&nbsp;Manajemen Sumber Daya Kesehatan 2 SKS&nbsp;&nbsp; </strong></p>
<p style="text-align: justify;">
	Dalam mata kuliah ini akan disampaikan tentang manajemen dan pengelolaan berbagai sumber daya yang ada di Organisasi Pelayanan Kesehatan (OPK), antara lain manajemen logistik dan peralatan, manajemen sumber daya manusia.</p>
<p>
	<strong>IKM 51913&nbsp;Keamanan Pangan dan Pencegahan Keracunan Makanan&nbsp;2 SKS</strong></p>
<p style="text-align: justify;">
	Mata ajar ini&nbsp; membahas evaluasi perjalanan mikroba dan zat-zat toskik dari industri dan sumber-sumber alami kedalam makanan dan selama proses produksi, penyiapan dan distribusi makanan. Dibahas juga bagaimana mengidentifikasi dan mengendalikan semua potensi bahaya keamanan pangan, dengan menggunakan pendekatan system, misal&nbsp; HACCP.</p>
<p>
	<strong>IKM 52113&nbsp;Keselamatan dan Kesehatan Kerja (K3) Lanjut&nbsp;2 SKS</strong></p>
<p style="text-align: justify;">
	Mata kuliah ini membahas penerapan prinsip keselamatan lanjut dan pencegahan kerugian, investigasi insiden dan analisis; alat manajemen keselamatan kerja, pengakuan kerugian, standar keselamatan, pedoman dan peraturan perundang-undangan. Juga mengulas penyakit akibat kerja oleh organ sasaran: musculo-skeletal, kulit, jantung, gangguan mental dan agresif pernapasan agen, trauma, agen fisik, pelarut dan logam.</p>
<p>
	<strong>IKM 52213&nbsp;Ergonomi &nbsp;2 SKS</strong></p>
<p style="text-align: justify;">
	Mata kuliah ini membahas keserasian manusia dan pekerjaannya&nbsp; Dibahas juga&nbsp; hubungan&nbsp; dan hubungan antara pekerjaan, peralatan, informasi, dan lingkungan yang serasi satu sama lainnya., yang bertujuan untuk mengurangi ketidaknyamanan pada saat bekerja sehingga pencegahan terhadap kelelahan kerja sedini mungkin sebelum berakibat kronis dan fatal.</p>
<p>
	<strong>MATA KULIAH PILIHAN&nbsp; </strong></p>
<p>
	<strong>SEMESTER 3 (4 SKS)</strong></p>
<p>
	<strong>IKM 61113&nbsp;Epidemiologi dan Pengendalian Penyakit Menular &nbsp;2 SKS</strong></p>
<p style="text-align: justify;">
	Mata ajaran ini membahas pendekatan epidemilogi dalam menjelaskan karakteristik penyakit menular yang umum terdapat di Indonesia; sebagai penyebab penyakit menular utama di Indonesia dan pengendaliannya.</p>
<p>
	<strong>IKM 61213&nbsp;Biostatistik Lanjut&nbsp;2 SKS</strong></p>
<p style="text-align: justify;">
	Mata kuliah ini membahas metode biostatistical lanjut untuk kesehatan masyarakat dan penelitian klinis dan mendapatkan keterampilan statistik yang diperlukan untuk menulis sebuah artikel jurnal atau laporan standar. Topik-topik yang luas yang akan dibahas dalam mata kuliah ini meliputi: analisis satu arah varians, analisis regresi linier sederhana dan berganda, strategi membangun model analisis regresi untuk menyesuaikan pembaur dan berurusan dengan efek modifikasi, analisis lanjutan data kategorikal, analisis regresi logistik untuk data hasil biner, analisis regresi untuk data count (Poisson dan regresi binomial negatif), analisis waktu untuk data peristiwa termasuk tabel kehidupan, Kaplan-Meier, log rank test dan Cox Model.</p>
<p>
	<strong>IKM 61313&nbsp;Ekonomi dan Pembiayaan Kesehatan 2 SKS&nbsp; </strong></p>
<p style="text-align: justify;">
	Di dalam mata kuliah ini membahas penerapan prinsip-prinsip ekonomi dan asuransi di bidang kesehatan baik secara makro maupun mikro, beserta contoh aplikasinya. Pada pertemuan selanjutnya akan dibahas tentang pembangunan ekonomi, kebijakan ekonomi, konsep <em>sustainable development</em>, efisiensi dan equity, <em>market failure</em>, externality, <em>public goods and private goods. </em>Selain itu akan dibahas pula tentang biaya dan teknik analisis biaya, pentarifan dan pembiayaan kesehatan, asuransi dan jaminan kesehatan masyarakat.</p>
<p>
	<strong>IKM 61413&nbsp;Pembuatan Kebijakan Kesehatan 2 SKS</strong></p>
<p style="text-align: justify;">
	Mata kuliah ini memberikan pemahaman tentang proses pembuatan suatu kebijakan. Aspek-aspek yang diajarkan adalah <em>stakeholders </em>pembuatan kebijakan, sumber daya dalam pembuatan kebijakan, mekanisme, serta cara pembuatan produk-produk kebijakan, RUU, Perpu, Perpres, Permen, Kepmen dan Perda. Mata kuliah ini membahas pembuatan kebijakan dengan memperhatikan aspek strategis terkait ekonomi, politik, sosial budaya, hukum dan keberpihakan agar produk kebijakan tersebut dapat diimplementasikan&nbsp; sesuai dengan lingkungan dan kebutuhannya.</p>
<p>
	<strong>IKM 61513&nbsp;<em>Current Issue</em> Kesehatan&nbsp;2 SKS </strong></p>
<p style="text-align: justify;">
	Mata kuliah ini membahas topik-topik mengenai kebijakan kesehatan yang sedang dijalankan pemerintah antara lain yang dapat dijadikan faktor-faktor penentu kesehatan global, komponen dan hubungan antar komponen&nbsp; sistem pelayanan kesehatan, berbagai sistem dan faktor penentu pelayanan kesehatan, organisasi penyelenggara pelayanan kesehatan, dukungan ekonomi dan manajemen dalam pelayanan kesehatan, penyelenggaraan pelayanan kesehatan, kecenderungan global dalam penyelenggeraan pelayanan kesehatan dan desentralisasi dalam penyelenggaraan pelayanan kesehatan.</p>
<p>
	<strong>IKM 61613 Penyakit Akibat Kerja (PAK)&nbsp; 2 SKS&nbsp; </strong></p>
<p style="text-align: justify;">
	Mata ajar ini&nbsp; membahas tentang penyakit ynag diderita pekerja dalam hubungan dengan kerja baik faktor risiko karena kondisi tempat kerja, perlatan kerja, material yang di pakai, proses produksi, cara kerja,&nbsp; dan sebagainya. Dibahas juga &nbsp;metode surveilans dalam mengenal PAK secara ilmiah serta tata cara klaim asuransi PAK.</p>
<p>
	<strong>IKM 61713&nbsp;Pengendalian Terpadu Vektor Penyakit Menular&nbsp;2 SKS</strong></p>
<p style="text-align: justify;">
	Mata ajar ini membahas penyakit-penyakit yang ditularkan oleh vektor artropoda termasuk cara-cara penularannya. Dibahas juga managemen pengendalian terpadu&nbsp; yang bertujuan untuk menurunkan densitas populasi vektor pada tingkat yang tidak membahayakan.</p>
<p>
	<strong>IKM 61813&nbsp;Manajemen Kebakaran dan Keadaan Darurat &nbsp;2 SKS</strong></p>
<p style="text-align: justify;">
	Mata kuliah ini membahas mengenai prinsip, konsep dan tujuan manajemen kebakaran. Dibahas juga mengenai Sistem Proteksi Kebakaran, manajemen Penanggulangan Kebakaran dan Sistem Tanggap Darurat.</p>
<p>
	<strong>IKM 61913&nbsp;Indoor Air Quality 2 SKS </strong></p>
<p style="text-align: justify;">
	Mata ajar ini membahas kualitas lingkungan dalam ruangan yang &nbsp;mengacu pada kualitas lingkungan bangunan dalam kaitannya dengan kesehatan dan kesejahteraan mereka yang menempati ruang di dalamnya. Dibahas juga &nbsp;banyak fakta yang mempengaruhinya&nbsp; dan dampaknya terhadap kesehatan manusia dan solusinya.</p>


					
