                               <strong>PROGRAM STUDI S2&nbsp; ILMU KESEHATAN MASYARAKAT</strong><br />
	<strong>PROGRAM PASCASARJANA UNSRI</strong></p>
<p>
	<strong>Gelar</strong><br />
	Magister Kesehatan Masyarakat, (M.K.M)</p>
<p style="text-align: justify;">
	<strong>Visi</strong></p>
<p style="text-align: justify;">
	Menjadi program studi yang unggul berbasis riset di bidang Ilmu Kesehatan Masyarakat di wilayah lahan basah tahun 2025.</p>
<p style="text-align: justify;">
	&nbsp;</p>
<p style="text-align: justify;">
	<strong>Misi</strong><br />
	Misi penyelenggaraan pendidikan Magister (S2) Program Studi Ilmu Kesehatan Masyarakat&nbsp; Universitas Sriwijaya adalah sebagai berikut:</p>
<p style="text-align: justify;">
	1. Menghasilkan lulusan yang bertaqwa kepada Tuhan Yang Maha Esa.</p>
<p style="text-align: justify;">
	2. Menyelenggarakan dan mengembangkan kegiatan pendidikan akademik di bidang Ilmu Kesehatan Masyarakat yang kompeten.</p>
<p style="text-align: justify;">
	3. Menyelenggarakan dan mengembangkan kegiatan penelitian bidang Ilmu Kesehatan Masyarakat melalui kerjasama dengan berbagai lembaga di tingkat nasional dan internasional.</p>
<p style="text-align: justify;">
	4. Menyelenggarakan dan mengembangkan pengabdian kepada masyarakat untuk membantu terwujudnya masyarakat yang sejahtera.</p>
<p style="text-align: justify;">
	5. Menyelenggarakan administrasi pendidikan tinggi yang modern, efektif dan efisien.</p>
<p style="text-align: justify;">
	&nbsp;</p>
<p style="text-align: justify;">
	<strong>Tujuan</strong></p>
<p style="text-align: justify;">
	Tujuan menyelenggarakan Program Magister (S2) Program Studi Ilmu Kesehatan Masyarakat&nbsp; Universitas Sriwijaya adalah :</p>
<p style="text-align: justify;">
	1. Menghasilkan magister kesehatan masyarakat yang bertaqwa kepada Tuhan Yang Maha Esa.</p>
<p style="text-align: justify;">
	2. Menghasilkan pengetahuan empiris, teoritis dan konseptual dalam bidang Ilmu Kesehatan Masyarakat.</p>
<p style="text-align: justify;">
	3. Menghasilkan magister yang berkompeten dalam mengaplikasikan ilmu kesehatan masyarakat melalui riset dan pengabdian pada&nbsp; &nbsp; &nbsp; &nbsp; masyarakat.</p>
<p style="text-align: justify;">
	4. Terwujudnya tata kelola yang baik pada Program Studi Magister Ilmu Kesehatan Masyarakat FKM Universitas Sriwijaya.</p>
<p>
	&nbsp;</p>
<p>
	<strong>Penyelenggaraan Pendidikan</strong></p>
<p>
	Program Studi S2 Ilmu Kesehatan Masyarakat memiliki 3 (tiga) bidang kajian utama (BKU), yaitu :</p>
<ol>
	<li>
		Epidemiologi dan Biostatistik (KME);</li>
	<li>
		Administrasi Kebijakan Kesehatan (KMA);</li>
	<li>
		Kesehatan Keselamatan Kerja (K3) dan Kesehatan Lingkungan (KMK).</li>
</ol>
<p>
	Setiap mahasiswa diwajibkan menempuh dan menyelesaikan beban studi minimal 37 SKS, dengan komposisi sebagai berikut :</p>
<ol>
	<li>
		Mata Kuliah Wajib Prodi 23 SKS;</li>
	<li>
		Mata Kuliah Wajib BKU 6 SKS;</li>
	<li>
		Mata Kuliah Pilihan 8 SKS.</li>
</ol>
