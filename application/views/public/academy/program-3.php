                               <strong>KURIKULUM PROGRAM STUDI </strong><strong>KESEHATAN LINGKUNGAN</strong></p>
<p align="center">
	<strong>PROGRAM </strong><strong>SARJANA </strong><strong>PADA FAKULTAS </strong><strong>KESEHATAN MASYARAKAT</strong><strong>&nbsp; UNIVESRITAS SRIWIJAYA TAHUN 201</strong><strong>7</strong></p>
<p style="margin-left:14.2pt;">
	&nbsp;</p>
<p style="margin-left:14.2pt;">
	<strong>A.&nbsp; </strong><strong>VISI</strong></p>
<p style="margin-left:14.2pt;">
	Menjadi Program Studi yang unggul di bidang Kesehatan Lingkungan di kawasan industri dan pemukiman lahan basah di tingkat nasional dan regional pada tahun 2025.</p>
<p style="margin-left:14.2pt;">
	&nbsp;</p>
<p style="margin-left:14.2pt;">
	<strong>B.&nbsp; </strong><strong>MISI</strong></p>
<p>
	1. &nbsp; &nbsp; &nbsp; &nbsp; Menyelenggarakan pendidikan dan pengajaran melalui proses belajar mengajar yang efisien dan efektif di bidang industri dan pemukiman lahan basah.</p>
<p>
	2. &nbsp; &nbsp; &nbsp; &nbsp; Melakukan penelitian dalam bidang kesehatan lingkungan yang dapat menyelesaikan masalah kesehatan lingkungan di kawasan industri dan pemukiman lahan basah.&nbsp;</p>
<p>
	3. &nbsp; &nbsp; &nbsp; &nbsp; Memberikan layanan dalam bentuk pengabdian masyarakat, pelatihan, seminar dan kerjasama dalam bidang kesehatan lingkungan khususnya kawasan industri dan pemukiman lahan basah.</p>
<p>
	4. &nbsp; &nbsp; &nbsp; &nbsp; Melakukan pengembangan jejaring ditingkat nasional dan regional di bidang pendidikan, penelitian dan pengabdian kepada masyarakat di bidang kesehatan lingkungan kawasan industri dan pemukiman lahan basah.</p>
<p>
	&nbsp;</p>
<p style="margin-left:14.2pt;">
	<strong>C.&nbsp; </strong><strong>TUJUAN</strong></p>
<p style="margin-left:36.0pt;">
	1.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Meningkatnya kualitas pembelajaran dan kemahasiswaan FKM Unsri</p>
<p style="margin-left:36.0pt;">
	2.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Meningkatnya kualitas kelembagaan FKM Unsri</p>
<p style="margin-left:36.0pt;">
	3.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Meningkatnya relevansi dan produktivitas penelitian dan pengembangankhususnya dibidang kesehatan lingkungan</p>
<p style="margin-left:36.0pt;">
	4.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Meningkatnya kapasitas inovasi dosen FKM Unsri</p>
<p style="margin-left:36.0pt;">
	5.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Meningkatnya relevansi, kualitas, dan kuantitas sumber daya FKM Unsri</p>
<p style="margin-left:36.0pt;">
	6.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Terwujudnya tata kelola yang baik pada semua unit kerja di lingkungan FKM Unsri</p>
<p style="margin-left:14.2pt;">
	&nbsp;</p>
<p style="margin-left:14.2pt;">
	<strong>D.&nbsp; </strong><strong>PROFIL LULUSAN</strong></p>
<p style="margin-left:14.2pt;">
	Profil lulusan Program Studi Kesehatan Lingkungan(Jenjang Sarjana) adalah:</p>
<p style="margin-left:36.0pt;">
	1.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; EHC (<em>Environmental Health <strong>C</strong>onsultant</em>)&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</p>
<p style="margin-left:36.0pt;">
	2.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; EHE (<em>Environmental Health <strong>E</strong>ducator</em>)&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</p>
<p style="margin-left:36.0pt;">
	3.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; EHR (Environmental Health <strong>R</strong>esearcher)&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</p>
<p style="margin-left:36.0pt;">
	4.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; EHM (Environmental Health <strong>M</strong>anager)&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</p>
<p style="margin-left:36.0pt;">
	5.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; EHO (<em>Environmental Health&nbsp; Officer/ <strong>A</strong>uditor</em>)&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</p>
<p style="margin-left:36.0pt;">
	6.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; EHS (<em>Environmental Health and Safety/ <strong>T</strong>echnician</em>)&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</p>
<p style="margin-left:36.0pt;">
	&nbsp;</p>
<p style="margin-left:14.2pt;">
	Yang memiliki kompetensi minimal sebagai berikut:</p>
<p style="margin-left:36.0pt;">
	1.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Sikap</p>
<p style="margin-left:2.0cm;">
	a.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Bertakwa kepada Tuhan Yang Maha Esa dan mampu menunjukkan<br />
	sikap religius</p>
<p style="margin-left:2.0cm;">
	b.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Menjunjung tinggi nilai kemanusiaan dalam menjalankan tugas<br />
	berdasarkan agama, moral, dan etika</p>
<p style="margin-left:2.0cm;">
	c.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Berkontribusi dalam peningkatan mutu kehidupan bermasyarakat,<br />
	berbangsa, bernegara, dan kemajuan peradaban berdasarkan Pancasila</p>
<p style="margin-left:2.0cm;">
	d.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Berperan sebagai warga negara yang bangga dan cinta tanah air, memiliki&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; nasionalisme serta rasa tanggungjawab pada negara dan bangsa</p>
<p style="margin-left:2.0cm;">
	e.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Menghargai keanekaragaman budaya, pandangan, agama, dan kepercayaan,&nbsp; serta pendapat atau temuan orisinal orang lain</p>
<p style="margin-left:2.0cm;">
	f.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Bekerja sama dan memiliki kepekaan sosial serta kepedulian terhadap&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; masyarakat dan lingkungan</p>
<p style="margin-left:2.0cm;">
	g.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Taat hukum dan disiplin dalam kehidupan bermasyarakat dan bernegara</p>
<p style="margin-left:2.0cm;">
	h.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Menginternalisasi nilai, norma, dan etika akademik</p>
<p style="margin-left:2.0cm;">
	i.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; menunjukkan sikap bertanggungjawab atas pekerjaan di bidang kesehatan lingkungansecara mandiri</p>
<p style="margin-left:2.0cm;">
	j.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Menginternalisasi semangat kemandirian, kejuangan, dan kewirausahaan</p>
<p style="margin-left:36.55pt;">
	&nbsp;</p>
<p>
	2.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Pengetahuan</p>
<p style="margin-left:2.0cm;">
	a.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Menguasai konsep dan teknik analisis masalah kesehatan lingkungan</p>
<p style="margin-left:2.0cm;">
	b.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Menguasai konsep kebijakan dan perencanaan program kesehatan lingkungan</p>
<p style="margin-left:2.0cm;">
	c.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Menguasai konsep dan teknik komunikasi efektif dalam memperbaiki kualitas kesehatan lingkungan</p>
<p style="margin-left:2.0cm;">
	d.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Memahami konsep budaya yang mempengaruhi kesehatan lingkungan</p>
<p style="margin-left:2.0cm;">
	e.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Menguasai konsep pemberdayaan masyarakat dalam upaya peningkatan kesehatan lingkungan</p>
<p style="margin-left:2.0cm;">
	f.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Menguasai konsep dan prinsip ilmu kesehatan lingkungan</p>
<p style="margin-left:2.0cm;">
	g.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Mengusai konsep perencanaan keuangan dan konsep manajemen terkait program kesehatan lingkungan</p>
<p style="margin-left:2.0cm;">
	h.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Menguasai konsep kepemimpinan dan berfikir sistem dalam membuat kebijakan kesehatan lingkungan</p>
<p style="margin-left:2.0cm;">
	&nbsp;</p>
<ol>
	<li>
		Keterampilan</li>
</ol>
<p style="margin-left:2.0cm;">
	a.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Mampu menerapkan konsep ilmu kesehatan lingkungan untuk meningkatkan status kesehatan masyarakat</p>
<p style="margin-left:2.0cm;">
	b.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Mampu melakukan kajian dan analisis situasi masalah kesehatan lingkungan</p>
<p style="margin-left:2.0cm;">
	c.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Mampu melakukan kajian terhadap budaya setempat (kearifan lokal)</p>
<p style="margin-left:2.0cm;">
	d.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Mampu berkomunikasi secara efektif</p>
<p style="margin-left:2.0cm;">
	e.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Mampu melaksanakan pemberdayaan masyarakat</p>
<p style="margin-left:2.0cm;">
	f.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Memiliki kemampuan kepemimpinan dan berfikir sistem</p>
<p style="margin-left:2.0cm;">
	g.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Mampu mengembangkan kebijakan dan perencanaan program kesehatan lingkungan</p>
<p style="margin-left:2.0cm;">
	h.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Mampu dalam merencanakan keuangan dan terampil dalam bidang manajemen kesehatan lingkungan</p>
