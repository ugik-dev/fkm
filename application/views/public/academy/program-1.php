                                <strong>Persyaratan Penerimaan Mahasiswa Baru Program S1<br />
                                </strong></p>
                            <p style="margin-left:13.5pt;">
                                Penerimaan mahasiswa baru di FKM Unsri mengikuti tata cara dan persyaratan yang ditetapkan oleh Universitas.</p>
                            <p style="margin-left:13.5pt;">
                                Secara umum ada 3 cara penerimaan mahasiswa baru, yaitu:</p>
                            <ul>
                                <li>
                                    Seleksi Nasional Masuk Perguruan Tinggi Negeri (SNMPTN)<br />
                                    Seleksi calon mahasiswa berasal dari lulusan Sekolah Lanjutan Tingkat Atas (SLTA) berdasarkan prestasi akademik siswa dan prestasi lain, sesuai dengan persyaratan penerimaan calon mahasiswa Universitas Sriwijaya melalui jalur undangan, yang terdiri dari 2 kategori berikut :</li>
                            </ul>
                            <ul>
                                <li>
                                    Seleksi Bersama Masuk Perguruan Tinggi Negeri (SBMPTN)<br />
                                    Seleksi calon mahasiswa yang berasal dari lulusan SLTA dengan mengikuti prosedur dan persyaratan penerimaan calon mahasiswa Universitas Sriwijaya melalui ujian tulis dan keterampilan.</li>
                            </ul>
                            <ul>
                                <li>
                                    Seleksi Mandiri (USM)<br />
                                    Seleksi calon mahasiswa yang diserahkan kepada PTN secara mandiri, dengan mengikuti prosedur dan persyaratan penerimaan calon mahasiswa Universitas Sriwijaya melalui ujian tulis dan keterampilan</li>
                            </ul>
                            <p>
                                &nbsp;</p>
                            <p>
                                Di Program Studi Ilmu Kesehatan Masyarakat (S1) Fakultas Kesehatan Masyarakat saat ini terdiri dari tujuh (7) peminatan, yaitu:</p>
                            <ol>
                                <li>
                                    Peminatan Administrasi Kebijakan Kesehatan (AKK)</li>
                                <li>
                                    Peminatan Promosi Kesehatan</li>
                                <li>
                                    Peminatan Gizi Masyarakat</li>
                                <li>
                                    Peminatan Kesehatan Lingkungan</li>
                                <li>
                                    Peminatan Keselamatan dan Kesehatan Kerja</li>
                                <li>
                                    Peminatan Epidemiologi</li>
                                <li>
                                    Peminatan Biostatistik</li>
                            </ol>
