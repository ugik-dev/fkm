                                <strong>KURIKULUM PROGRAM STUDI GIZI</strong></p>
                            <p align="center">
                                <strong>PROGRAM SARJANA PADA FAKULTAS KESEHATAN MASYARAKAT</strong></p>
                            <p align="center">
                                <strong>UNIVERSITAS SRIWIJAYA TAHUN 2017</strong></p>
                            <p align="center">
                                &nbsp;</p>
                            <p style="margin-left:14.0pt;">
                                <strong>A.&nbsp; </strong><strong>VISI</strong></p>
                            <p style="margin-left:14.2pt;">
                                Menjadi Institusi Pendidikan Ilmu Gizi yang unggul di Indonesia yang berbasis riset dan dapat bersaing secara Global.</p>
                            <p>
                                &nbsp;</p>
                            <p style="margin-left:14.0pt;">
                                <strong>B.&nbsp; </strong><strong>MISI</strong></p>
                            <p style="margin-left:1.0cm;">
                                1.&nbsp;&nbsp;&nbsp; Menyelenggarakan&nbsp; pendidikan dan pengajaran melalui proses belajar mengajar yang efisien dan efektif dengan mendayagunakan sumber daya manusia dan sarana belajar seoptimal mungkin.</p>
                            <p style="margin-left:1.0cm;">
                                2.&nbsp;&nbsp;&nbsp; Melakukan penelitian dalam bidang gizi yang dapat menyelesaikan masalah gizi di tingkat nasional, regional dan internasional.</p>
                            <p style="margin-left:1.0cm;">
                                3.&nbsp;&nbsp;&nbsp; Memberikan layanan dalam bentuk pengabdian masyarakat, pelatihan, seminar dan kerjasama dalam pengembangan pelayanan gizi dengan institusi lainnya.</p>
                            <p style="margin-left:1.0cm;">
                                4.&nbsp;&nbsp;&nbsp; Melakukan pengembangan jejaring ditingkat nasional, regional dan internasional dalam bidang pendidikan, penelitian dan pengabdian kepada masyarakat.</p>
                            <p style="margin-left:1.0cm;">
                                &nbsp;</p>
                            <p style="margin-left:14.0pt;">
                                <strong>C.&nbsp; </strong><strong>TUJUAN</strong></p>
                            <p style="margin-left:1.0cm;">
                                1.&nbsp;&nbsp;&nbsp; Menghasilkan lulusan yang memiliki integritas kepribadian yang tinggi sebagai tenaga yang bertaqwa, bermutu, tanggap terhadap perubahan dan kemajuan ilmu pengetahuan dan teknologi serta berbagai masalah yang dihadapi masyarakat khususnya yang berkaitan dengan gizi, pangan dan kesehatan.</p>
                            <p style="margin-left:1.0cm;">
                                2.&nbsp;&nbsp;&nbsp; Menghasilkan lulusan yang dinamis yang mampu menerapkan pengetahuan dan keterampilannya dalam mengembangkan manajemen pelayanan gizi secara profesional yang berorientasi pada standar mutu profesi gizi kepada masyarakat baik secara individu maupun kelompok.</p>
                            <p style="margin-left:1.0cm;">
                                3.&nbsp;&nbsp;&nbsp; Meningkatkan kapabilitas dan mutu dosen sehingga dapat berinovasi dan berdaya saing secara internasional.</p>
                            <p style="margin-left:1.0cm;">
                                4.&nbsp;&nbsp;&nbsp; Meningkatkan produktifitas penelitian dan publikasi ilmiah dalam bidang gizi, pangan dan kesehatan</p>
                            <p style="margin-left:1.0cm;">
                                5.&nbsnbsp;&nbsp;&nbsp; Mewujudkan tata kelola yang bermutu sebagai basis kegiatan profesionalisme lembaga/ institusi.</p>
                            <p style="margin-left:1.0cm;">
                                &nbsp;</p>
                            <p style="margin-left:14.0pt;">
                                <strong>D.&nbsp; </strong><strong>PROFIL LULUSAN</strong></p>
                            <p style="margin-left:14.0pt;">
                                Profil lulusan Program Studi Ilmu Kesehatan Masyarakat (Jenjang Sarjana) adalah:</p>
                            <p style="margin-left:35.0pt;">
                                1.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Decision Maker</p>
                            <p style="margin-left:35.0pt;">
                                2.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Manajer and Care Provider</p>
                            <p style="margin-left:35.0pt;">
                                3.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Supervisor Pelayanan Gizi</p>
                            <p style="margin-left:35.0pt;">
                                4.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Supervisor Pendidikan dan Pelatihan</p>
                            <p style="margin-left:35.0pt;">
                                5.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Community Leader</p>
                            <p style="margin-left:35.0pt;">
                                6.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Researcher</p>
                            <p style="margin-left:14.2pt;">
                                &nbsp;</p>
                            <p style="margin-left:14.2pt;">
                                Yang memiliki kompetensi minimal sebagai berikut:</p>
                            <p>
                                1.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Sikap</p>
                            <p style="margin-left:2.0cm;">
                                a.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Bertakwa kepada Tuhan Yang Maha Esa dan mampu menunjukkan<br />
                                sikap religius</p>
                            <p style="margin-left:2.0cm;">
                                b.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Menjunjung tinggi nilai kemanusiaan dalam menjalankan tugas<br />
                                berdasarkan agama, moral, dan etika</p>
                            <p style="margin-left:2.0cm;">
                                c.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Berkontribusi dalam peningkatan mutu kehidupan bermasyarakat,<br />
                                berbangsa, bernegara, dan kemajuan peradaban berdasarkan Pancasila</p>
                            <p style="margin-left:2.0cm;">
                                d.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Berperan sebagai warga negara yang bangga dan cinta tanah air, memiliki&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; nasionalisme serta rasa tanggungjawab pada negara dan bangsa</p>
                            <p style="margin-left:2.0cm;">
                                e.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Menghargai keanekaragaman budaya, pandangan, agama, dan kepercayaan,&nbsp; serta pendapat atau temuan orisinal orang lain</p>
                            <p style="margin-left:2.0cm;">
                                f.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Bekerja sama dan memiliki kepekaan sosial serta kepedulian terhadap&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; masyarakat dan lingkungan</p>
                            <p style="margin-left:2.0cm;">
                                g.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Taat hukum dan disiplin dalam kehidupan bermasyarakat dan bernegara</p>
                            <p style="margin-left:2.0cm;">
                                h.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Menginternalisasi nilai, norma, dan etika akademik</p>
                            <p style="margin-left:2.0cm;">
                                i.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; menunjukkan sikap bertanggungjawab atas pekerjaan di bidang kesehatan lingkungan secara mandiri</p>
                            <p style="margin-left:2.0cm;">
                                j.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Menginternalisasi semangat kemandirian, kejuangan, dan kewirausahaan</p>
                            <p style="margin-left:36.55pt;">
                                &nbsp;</p>
                            <p>
                                2.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Pengetahuan</p>
                            <p style="margin-left:2.0cm;">
                                a.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Menguasai konsep dan teknik analisis masalah gizi</p>
                            <p style="margin-left:2.0cm;">
                                b.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Menguasai konsep kebijakan dan perencanaan program gizi</p>
                            <p style="margin-left:2.0cm;">
                                c.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Menguasai konsep dan teknik komunikasi efektif dalam memperbaiki kualitas gizi</p>
                            <p style="margin-left:2.0cm;">
                                d.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Memahami konsep budaya yang mempengaruhi gizi</p>
                            <p style="margin-left:2.0cm;">
                                e.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Menguasai konsep pemberdayaan masyarakat dalam upaya peningkatan gizi</p>
                            <p style="margin-left:2.0cm;">
                                f.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Menguasai konsep dan prinsip ilmu gizi</p>
                            <p style="margin-left:2.0cm;">
                                g.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Mengusai konsep perencanaan keuangan dan konsep manajemen terkait program gizi</p>
                            <p style="margin-left:2.0cm;">
                                h.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Menguasai konsep kepemimpinan dan berfikir sistem dalam membuat kebijakan gizi</p>
                            <p style="margin-left:2.0cm;">
                                &nbsp;</p>
                            <ol>
                                <li>
                                    Keterampilan</li>
                            </ol>
                            <p style="margin-left:2.0cm;">
                                a.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Mampu menerapkan konsep ilmu gizi untuk meningkatkan status kesehatan masyarakat</p>
                            <p style="margin-left:2.0cm;">
                                b.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Mampu melakukan kajian dan analisis situasi masalah gizi</p>
                            <p style="margin-left:2.0cm;">
                                c.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Mampu melakukan kajian terhadap budaya setempat (kearifan lokal)</p>
                            <p style="margin-left:2.0cm;">
                                d.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Mampu berkomunikasi secara efektif</p>
                            <p style="margin-left:2.0cm;">
                                e.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Mampu melaksanakan pemberdayaan masyarakat</p>
                            <p style="margin-left:2.0cm;">
                                f.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Memiliki kemampuan kepemimpinan dan berfikir sistem</p>
                            <p style="margin-left:2.0cm;">
                                g.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Mampu mengembangkan kebijakan dan perencanaan program gizi</p>
                            <p style="margin-left:2.0cm;">
                                h.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Mampu dalam merencanakan keuangan dan terampil dalam bidang manajemen gizi</p>
