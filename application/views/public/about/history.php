                            <p align="center">
                                <strong>SEJARAH PROGRAM STUDI KESEHATAN MASYARAKAT<br />
                                    FAKULTAS KEDOKTERAN UNSRI</strong></p>
                            <p>
                                &nbsp;</p>
                            <p style="text-align: justify;">
                                September 1996, Dekan Fakultas Kedokteran Universitas Sriwijaya dr. H Rusdi Ismail, DSAK mengemukakan gagasan pembentukan program studi baru di Fakultas Kedokteran Universitas Sriwijaya. Gagasan tersebut timbul karena adanya himbauan dari Direktur Jendral Pendidikan Tinggi dan Konsorsium Ilmu Kedokteran pada rapat dekan seluruh Indonesia di Jakarta agar FK Unsri ikut menyiapkan sumber daya manusia di bidang kedokteran dan kesehatan dalam menyongsong era globalisasi. Gagasan ini disampaikan di dalam rapat senat Fakultas Kedokteran Universitas Sriwijaya, sebagai tindak lanjutnya, dekan membentuk 3 satuan tugas yaitu :</p>
                            <ul>
                                <li style="text-align: justify;">
                                    Satuan tugas Program Studi Biomedik (S2) yang di ketuai oleh dr. KHM Arsyad, DABK, Sp.And.</li>
                                <li style="text-align: justify;">
                                    Satuan tugas Program Studi Kesehatan Masyarakat dan Keperawatan (S1) diketuai oleh dr. H.MA Husnil Farouk, MPH.</li>
                                <li style="text-align: justify;">
                                    Satuan tugas Program Studi D3 Teknik Gigi yang diketuai oleh drg. Ny. Prasanto.</li>
                            </ul>
                            <p style="text-align: justify;">
                                Satuan tugas ini dikukuhkan oleh surat Keputusan Rektor Unsri Prof. Ir. H Machmud Hasyim, MME, No : 3483/PT11.1.1/C.6.f/1996 tanggal 1 Agustus 1996, satuan tugas Program Studi Kesehatan Masyarakat dan Keperawatan beranggotakan :</p>
                            <p style="text-align: justify;">
                                <br />
                                <strong>Pengarah</strong><br />
                                1. Rektor Universitas Sriwijaya.<br />
                                2. Pembantu Rektor I Unsri.<br />
                                <br />
                                <strong>Nara Sumber</strong><br />
                                1. Kakanwil Depkes Prop. Sumatera Selatan.<br />
                                2. Ketua IAKMI Cabang Palembang.<br />
                                3. Ketua IDKI Cabang Palembang.<br />
                                4. Kepala Balai Hyperkes Palembang.<br />
                                5. Ketua Bagian IKM-IKK FK Unsri.<br />
                                <br />
                                <strong>Penanggung jawab</strong><br />
                                Dekan Fakultas Kedokteran Unsri.<br />
                                <br />
                                <strong>Panitia Pelaksana</strong><br />
                                <br />
                                <strong>Ketua</strong><br />
                                dr. H.M.A Husnil Farouk, MPH.<br />
                                <br />
                                <strong>Sekretaris</strong><br />
                                dr. H.M. Ridwan MOH, MSc.<br />
                                <br />
                                <strong>Anggota</strong><br />
                                1. dr. H. Ali Mochtar, MSc.<br />
                                2. dr. H. Chairil Zaman, Msc.<br />
                                3. H. Muhaimin, S.KM.<br />
                                4. dr. Hendarmin Aulia, SU.<br />
                                5. dr. M. Zulkarnain, M.Med.Sc.<br />
                                6. dr. Fahmi Idris.</p>
                            <p style="text-align: justify;">
                                Hasil rapat koordinasi peningkatan personalia dan pendayagunaan dokter spesialis di Bandung pada bulan Februari 1999 yang dihadiri oleh Menteri Kesehatan RI Prof. dr. F.A. Moeloek, Dekan FK Unsri Prof. Dr. KHM Arsyad, DABK, SpAnd, kembali diingatkan oleh konsorsium untuk bersedia membuka program studi di bidang kesehatan masyarakat, ilmu keperawatan dan kedokteran gigi.</p>
                            <p style="text-align: justify;">
                                Sebagai tindak lanjutnya dekan membentuk satuan tugas Program Studi Kesehatan Masyarakat (PSKM) dikukuhkan dengan surat Keputusan Rektor Unsri Bapak Prof. Dr. Ir. H. Zainal Ridho Djafar, Msc nomor : 2357/PT11.1.1/C.6.f/1999 tanggal 7 Mei 1999.</p>
                            <p style="text-align: justify;">
                                <br />
                                Susunan satuan tugas tersebut adalah :<br />
                                <br />
                                <strong>Ketua</strong><br />
                                dr. H.M.A Husnil Farouk, MPH.<br />
                                <strong><br />
                                    Sekretaris</strong><br />
                                dr. Hendarmin Aulia, SU.<br />
                                <strong><br />
                                    Anggota</strong><br />
                                1. dr. Ida Fitrianti (Kanwil Depkes Prop Sumatera Selatan).<br />
                                2. H. Muhaimin, S.KM (Bapelkes Prop. Sumatera Selatan).<br />
                                3. Dra. Herawati, M.Kes (Kanwil Depkes Prop Sumsel).<br />
                                4. Amar Muntaha, S.KM.,M.Kes (Kanwil Depkes Prop Sumsel).<br />
                                5. A. Fikri Faisya, S.KM., M.Kes (Kanwil Depkes Prop Sumsel).<br />
                                6. dr. Mohammad Zulkarnain, M.Med.Sc.<br />
                                7. Martawan Madari, S.KM (Kanwil Depkes Prop Sumsel).</p>
                            <p style="text-align: justify;">
                                Satuan tugas berhasil membuat proposal yang disampaikan oleh pimpinan Fakultas kepada Rektor dan selanjutnya diteruskan ke Menteri Pendidikan.</p>
                            <p style="text-align: justify;">
                                Menteri Kesehatan juga sangat mendukung dibukanya PSKM, ini dengan diterbitkannya SK no 1215/Menkes/X/1999 tanggal 15 Oktober 1999. dr. Syahrial Aziz, DAFK selaku Pembantu Dekan II FK Unsri berperan aktif membantu satuan tugas dalam proses terbitnya surat Keputusan Dirjen Dikti Departemen Pendidikan Nasional. Sambil menunggu turunnya surat Keputusan Dirjen Dikti, Fakultas Kedokteran Unsri mengadakan seminar regional yang bertema Menyongsong Era Globalisasi dan Indonesia Sehat 2010 dengan pembicara utama Presiden Executive BKS FKM Prof. Dr. H. Does Sampoerno, MPH., mengadakan konsultasi dengan ketua CHS Prof Ma&#39;rifin Husin dan mengadakan kerjasama dengan Fakultas Kesehatan Masyarakat Universitas Indonesia untuk mempersiapkan segala sesuatu yang berkaitan dengan pembukaan PSKM di Fakultas Kedokteran Unsri.</p>
                            <p style="text-align: justify;">
                                Terhitung tanggal 18 Agustus 2000, resmi di buka Program Studi (S1) Kesehatan Masyarakat Fakultas Kedokteran Unsri dengan surat Keputusan Dirjen Dikti No 274/Dikti/Kep/2000. Perkembangan ilmu pengetahuan dan teknologi di bidang kesehatan masyarakat sangat cepat. Akibat pesatnya perkembangan tersebut sebahagian besar aktifitas kehidupan manusia tidak terlepas dari pemanfaatan pengetahuan dan teknologi kesehatan tersebut.<br />
                                <br />
                                Konsekuensi penggunaan pengetahuan dan teknologi kesehatan tersebut, tentunya memerlukan sumber daya manusia yang tidak sedikit. Sumber daya manusia yang dibutuhkan adalah sumber daya manusia yang memiliki kemampuan mengembangkan dan menyebarluaskan ilmu pengetahuan dan teknologi kesehatan. Untuk menyikapi kebutuhan sumber daya manusia tersebut, perguruan tinggi sebagai penghasil tenaga ahli berpendidikan tinggi, berupaya untuk menghasilkan lulusan yang memiliki kompetensi sebagai tenaga ahli di bidang teknologi kesehatan masyarakat. Secara organisatoris PSKM, masih bernaung di bawah Fakultas Kedokteran Universitas Sriwijaya bersama dengan Program Studi Ilmu Keperawatan dan Program Studi Kedokteran Gigi dan telah menyelenggarakan kegiatan tridharma perguruan tinggi. Organisasi dan tata kerja, manajemen sumber daya manusia, manajemen keuangan, manajemen infrastruktur dan fasilitas telah dilaksanakan secara mandiri dan swakelola.<br />
                                <br />
                                Berdasarkan aspek pengembangan ilmu kesehatan dan penyelenggaraan administrasi dan manajemen program, Kemudian Program Studi Kesehatan Masyarakat Fakultas Kedokteran (PSKM FK) Unsri diajukan menjadi Fakultas Kesehatan Masyarakat (FKM) Unsri. Dengan menjadi Fakultas dapat ditata organisasi dan manajemen yang lebih baik dan stabil, jaminan mutu pelaksanaan kegiatan akademik, pelaksanaan kerja sama, baik di dalam maupun di luar negeri, adanya lembaga Senat Fakultas, adanya unit penelitian dan pengabdian masyarakat. Hal penting lainnya yang diharapkan dengan terbentuknya fakultas adalah peningkatan sinergi internal, baik secara horisontal dengan fakultas yang lain maupun vertikal dengan Rektorat dan sinergi eksternal yang berhubungan dengan kepentingan masyarakat dan stakeholder , diantaranya mencapai Sumatera Selatan sehat 2008 dan Indonesia sehat 2010. PSKM FK Unsri yang mempunyai kekuatan hukum dengan SK pendirian dari Direktur Jenderal Pendidikan Tinggi Departemen Pendidikan Nasional No. 274/DIKTI/Kep/2000 dan surat keputusan penyelenggaraan pendidikan oleh Rektor Unsri No. VIII-1313/PT11.1.2/Q/2000.</p>
                            <p style="text-align: justify;">
                                <strong><br />
                                    ALIH STATUS DARI PROGRAM STUDI KESEHATAN MASYARAKAT FAKULTAS KEDOKTERAN UNSRI MENJADI<br />
                                    FAKULTAS KESEHATAN MASYARAKAT UNIVERSITAS SRIWIJAYA</strong></p>
                            <p style="text-align: justify;">
                                Berdasarkan keputusan badan akreditasi nasional perguruan tinggi nomor : 012/BAN-PT/AK-XI/S1/VI/2008 tentang status, peringkat, dan hasil akreditasi program sarjana di perguruan tinggi terhitung tanggal 28 Juni 2008, telah dilakukan akreditasi oleh tim asesor BAN DIKTI, dengan perolehan akreditasi B sampai dengan tanggal 28-06-2013. Telah dilakukan perubahan status dari PSKM-FK Unsri menjadi FKM Unsri, berdasarkan surat Direktur Jenderal Pendidikan Tinggi Depdiknas nomor 3907/D/T/2008 tanggal 7 November 2008, prihal pendirian Fakultas Kesehatan Masyarakat Unsri dan SK Rektor Unsri nomor : 1712/H9/PE/2008 tanggal 20 November 2008, tentang pendirian FKM Unsri yang saat ini menempati lokasi yang cukup strategis, yaitu di gedung Dr. A.I. Muthalib, M.PH (CHEREC), Universitas Sriwijaya Kampus Inderalaya, yang diresmikan oleh Direktur Jenderal Pendidikan Tinggi, Departemen Pendidikan Nasional Republik Indonesia, Prof. Dr. Ir. Satryo Soemantri Brodjonegoro Pada tanggal 11 Oktober 2004. Untuk meningkatkan kualitas kegiatan belajar mengajar, dilakukan beberapa upaya pembenahan di FKM Unsri antara lain dengan dikeluarkannya surat tugas dari pimpinan Fakultas nomor : 111/H9.1.4/Pend/ M.1FKM/2009 prihal pembentukan tim revisi kurikulum Fakultas Kesehatan Masyarakat Unsri yang diketuai oleh Prof. dr. Tan Malaka, MOH, PHD. Tim ini bertugas antara lain melakukan studi banding kurikulum di FKM Universitas Indonesia Depok dan FKM Universitas Airlangga Surabaya, sehingga ke depan akan diperoleh kurikulum Fakultas Kesehatan Masyarakat Unsri yang sesuai dengan kebutuhan pasar, budaya, potensi dan karakteristik lokal.<br />
                                <br />
                                Dengan dikeluarkannya surat tugas dari pimpinan FKM Unsri nomor : 111/H9.1.4/Pend/ 1.3/ FKM/2009, prihal pembentukan tim pengumpul data rapat kerja Unsri tahun 2008/2009 berdasarkan SK Rektor Unsri nomor 372/H9/LL/2009, yang diketuai oleh :<br />
                                <br />
                                Hamzah Hasyim,S.KM, M.KM. FKM Unsri sebagai Fakultas yang ke-10 dalam lingkungan Unsri, untuk yang pertama kalinya ikut serta dalam kegiatan rapat kerja, di Kantor Pusat Administrasi Unsri tanggal 7-8 April 2009 dengan tema Melangkah Maju Ke Universitas Kelas Dunia.<br />
                                <br />
                                Tim yang dibentuk diberikan kesempatan untuk memaparkan kinerja tahunan FKM meliputi pengukuran kinerja tahun 2009 dan rencana kerja tahun 2010 yang terdiri dari program pemerataan dan perluasan akses pendidikan dan pembelajaran, program peningkatan mutu, relevansi dan daya saing, pendidikan dan pembelajaran serta program governance , akuntabilitas dan pencitraan publik, hasil paparan ini mendapatkan apresiasi positif dari pimpinan Unsri. Dalam kurun waktu 9 tahun sejak tahun 2000, Unsri yang telah menyelenggarakan PSKM FK Unsri dan beralih status menjadi FKM Unsri telah menghasilkan lulusan (alumni) dengan gelar/sebutan Sarjana Kesehatan Masyarakat (S.KM) yang telah bekerja di instansi kesehatan dan luar kesehatan di hampir seluruh wilayah nusantara. Harapan beberapa tahun ke depan dapat menghasilkan tenaga ahli Kesehatan Masyarakat program S1 2 tahun, program studi baru dan program strata 2 (Magister Kesehatan Masyarakat).</p>

