                                <p style="text-align: justify;">
                                    <strong>Keterangan Lambang</strong></p>
                                <ol>
                                    <li style="text-align: justify;">
                                        Bunga Melati merupakan bunga suci melambangkan kemurnian, keanggunan, keluhuran, wibawa dan kesetiaan pada cita-cita.&nbsp; Pada lambang digambarkan lima mahkota bunga (corolla) dengan warna kuning muda.&nbsp; Lima mahkota bunga melambangkan Pancasila sebagai falsafah hidup bangsa Indonesia, sedangkan warna kuning muda melambangkan warna Universitas.</li>
                                    <li style="text-align: justify;">
                                        Bunga Seruni sebagai lambang yang paling tua ditemukan dalam sejarah Sriwijaya. Pada lambang digambarkan mahkota bunga (corolla) dari bunga yang terpilin (marginal flower), berjumlah 31, berwarna kuning emas yang terpilin. Jumlah 31 melambangkan tanggal lahir Unsri pada tanggal 31 Oktober 1960 dan warna kuning emas melambangkan keagungan Sriwijaya. Mahkota bunga yang terpilin ke kanan yang mengakibatkan ujung kelopaknya mengarah ke kiri dimaksudkan bahwa Universitas ini berjalan seirama dengan perputaran jaman.</li>
                                    <li style="text-align: justify;">
                                        Cahaya melambangkan ilmu pengetahuan, yang menerangi jagad, menunjukan bahwa hanya dengan ILMU ketidaktahuan dapat ditiadakan. Ilmu digambarkan dengan cahaya (nur) sebanyak 60 pancaran sinar dengan 10 sinar besar, berarti bulan Oktober 1960.</li>
                                    <li style="text-align: justify;">
                                        Kata-kata UNIVERSITAS SRIWIJAYA ditulis dengan huruf putih di atas dasar hitam yang menlingkar di dalam bunga seruni. Hitam melembangkan ketidaktahuan. Dengan adanya cahaya, ketidaktahuan dapat ditiadakan, yang lambangkan dengan huruf putih pada kata-kata UNIVERSITAS SRIWIJAYA menandakan Unsri sebagai gudang ilmu</li>
                                    <li style="text-align: justify;">
                                        Motto ILMU ALAT PENGABDIAN ditulis dengan huruf berwarna emas di atas dasar hitam yang terletak dalam selendang dibagian bawah lambang. Motto ini melambangkan bahwa manusia wajib mengabdi kepada Tuhan, negara , bangsa, masyarakat dan keluarga. Pengabdian yang tidak disertai dengan ilmun pengetahuan tidaklah sempurna.</li>
                                </ol>
                                <p style="text-align: justify;">
                                    <strong>Makna lambang</strong></p>
                                <p style="text-align: justify;">
                                    Unsri sebagai milik bangsa Indonesia yang berfalsafah hidup Pancasila selalu dengan penuh daya dan dinamika meningkatkan ilmu pengetahuan guna memerangi ketidaktahuan yang merupakan penghalang bagi masyarakat bangsa Indonesia untuk mencapai cita-cita masyarakat adil dan makmur serta dalam ridho Tuhan Yang Maha Esa.</p>



